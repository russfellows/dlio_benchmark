def test_gen_random_tensor_seed_reproducibility():
    """Same seed must produce byte-identical arrays on every call."""
    arr1 = gen_random_tensor(shape=(64, 64), dtype=np.float32, seed=42)
    arr2 = gen_random_tensor(shape=(64, 64), dtype=np.float32, seed=42)
    np.testing.assert_array_equal(arr1, arr2,
        err_msg="gen_random_tensor with same seed produced different arrays")


def test_gen_random_tensor_seed_uniqueness():
    """Different seeds must produce different arrays."""
    arr0 = gen_random_tensor(shape=(128,), dtype=np.float32, seed=10)
    arr1 = gen_random_tensor(shape=(128,), dtype=np.float32, seed=11)
    assert not np.array_equal(arr0, arr1), (
        "Seeds 10 and 11 produced identical arrays — seeding logic is broken")


def test_gen_random_tensor_no_seed_entropy():
    """Two entropy-seeded calls must produce different arrays."""
    arr1 = gen_random_tensor(shape=(128,), dtype=np.float32, seed=None)
    arr2 = gen_random_tensor(shape=(128,), dtype=np.float32, seed=None)
    assert not np.array_equal(arr1, arr2), (
        "Entropy-seeded gen_random_tensor produced identical arrays — "
        "entropy seeding may be broken")


def test_gen_random_tensor_rng_flow_through():
    """Passing the same rng object must yield different data each call (flow-through)."""
    rng = np.random.default_rng(seed=99)
    arr1 = gen_random_tensor(shape=(256,), dtype=np.float32, rng=rng)
    arr2 = gen_random_tensor(shape=(256,), dtype=np.float32, rng=rng)
    assert not np.array_equal(arr1, arr2), (
        "Passing the same rng twice produced identical arrays — "
        "flow-through is not advancing the RNG state")


def test_gen_random_tensor_rng_reproducibility():
    """Two rngs with the same seed must produce the same sequence."""
    rng_a = np.random.default_rng(seed=7)
    rng_b = np.random.default_rng(seed=7)
    a1 = gen_random_tensor(shape=(128,), dtype=np.float32, rng=rng_a)
    a2 = gen_random_tensor(shape=(128,), dtype=np.float32, rng=rng_a)
    b1 = gen_random_tensor(shape=(128,), dtype=np.float32, rng=rng_b)
    b2 = gen_random_tensor(shape=(128,), dtype=np.float32, rng=rng_b)
    np.testing.assert_array_equal(a1, b1, err_msg="First call differed with same seed")
    np.testing.assert_array_equal(a2, b2, err_msg="Second call differed with same seed")


def test_gen_random_tensor_mpi_rank_seeds_differ():
    """Simulated per-rank seeds must produce statistically distinct arrays."""
    BASE_SEED = DataGenerator.BASE_SEED
    rank0 = gen_random_tensor(shape=(128,), dtype=np.float32, seed=BASE_SEED + 0)
    rank1 = gen_random_tensor(shape=(128,), dtype=np.float32, seed=BASE_SEED + 1)
    assert not np.array_equal(rank0, rank1), (
        "BASE_SEED+0 and BASE_SEED+1 produced identical arrays")


def test_gen_random_tensor_integer_dtype():
    """Seeds must work for integer dtypes."""
    arr1 = gen_random_tensor(shape=(256,), dtype=np.uint8, seed=77)
    arr2 = gen_random_tensor(shape=(256,), dtype=np.uint8, seed=77)
    np.testing.assert_array_equal(arr1, arr2)


def test_gen_random_tensor_data_entropy():
    """Generated data must have high entropy — zlib cannot compress it below 95%."""
    data = gen_random_tensor(shape=(65536,), dtype=np.uint8, seed=42)
    raw = data.tobytes()
    compressed = zlib.compress(raw, level=9)
    ratio = len(compressed) / len(raw)
    assert ratio > 0.95, (
        f"Generated data compressed to {ratio:.1%} — entropy is suspiciously low. "
        "dgen-py or NumPy RNG output should be near-incompressible.")


