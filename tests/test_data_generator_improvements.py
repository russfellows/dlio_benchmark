@pytest.mark.timeout(TEST_TIMEOUT_SECONDS * 2, method="thread")
def test_npy_reproducibility():
    """NPY generator must produce bit-identical files on two consecutive runs."""
    init()
    clean()

    overrides = _BASE_OVERRIDES + [
        "++workload.dataset.format=npy",
        "++workload.dataset.num_files_train=4",
        "++workload.dataset.num_files_eval=2",
        "++workload.dataset.num_samples_per_file=4",
        "++workload.dataset.record_length=1024",
    ]

    hashes_run1 = {}
    with initialize_config_dir(version_base=None, config_dir=config_dir):
        cfg = compose(config_name="config", overrides=overrides)
        run_benchmark(cfg, verify=False)
        if comm.rank == 0:
            data_folder = cfg.workload.dataset.data_folder
            for split in ("train", "valid"):
                for p in sorted(pathlib.Path(f"{data_folder}/{split}").rglob("*.npy")):
                    hashes_run1[f"{split}/{p.name}"] = _sha256(str(p))

    comm.Barrier()
    if comm.rank == 0:
        shutil.rmtree("./data/", ignore_errors=True)
    comm.Barrier()

    hashes_run2 = {}
    with initialize_config_dir(version_base=None, config_dir=config_dir):
        cfg2 = compose(config_name="config", overrides=overrides)
        run_benchmark(cfg2, verify=False)
        if comm.rank == 0:
            data_folder2 = cfg2.workload.dataset.data_folder
            for split in ("train", "valid"):
                for p in sorted(pathlib.Path(f"{data_folder2}/{split}").rglob("*.npy")):
                    hashes_run2[f"{split}/{p.name}"] = _sha256(str(p))

    if comm.rank == 0:
        assert hashes_run1 == hashes_run2, (
            "NPY generator is NOT reproducible across runs.\n"
            f"Run 1 hashes: {hashes_run1}\n"
            f"Run 2 hashes: {hashes_run2}")

    clean()
    finalize()
