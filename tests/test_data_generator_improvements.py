"""
Tests for data generator quality, correctness, and reader compatibility.

Philosophy:
  Tests must validate CORRECT behaviour, not just confirm that code runs.
  A test that always passes regardless of the implementation is worse than
  no test — it creates false confidence.

Test groups:

1. **Unit tests** — no MPI, no file I/O.
   * ``gen_random_tensor`` seed reproducibility, uniqueness, and entropy.
   * ``DataGenerator`` class constants and static helpers.
   * RNG flow-through (same rng object → different output each call).

2. **Format correctness** — generate files, open them with the native library
   and verify *structural* correctness (dtype, shape, keys, schema).

3. **Data uniqueness** — generated files must contain non-identical data both
   *across* files and *within* a file (sample-to-sample).

4. **Reader compatibility** — files generated by each generator must be opened
   and parsed correctly by the matching DLIO reader class.

5. **Reproduciblity** — the same configuration must produce bit-identical files
   on successive runs.

6. **Entropy / non-compressibility** — generated data must be high-entropy and
   resistant to trivial compression (validates dgen-py or uniform-RNG output).
"""

