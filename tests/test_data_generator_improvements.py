def clean(storage_root="./"):
    comm.Barrier()
    if comm.rank == 0:
        shutil.rmtree(os.path.join(storage_root, "checkpoints"), ignore_errors=True)
        shutil.rmtree(os.path.join(storage_root, "data/"), ignore_errors=True)
        shutil.rmtree(os.path.join(storage_root, DLIO_TEST_OUTPUT_DIR), ignore_errors=True)
    comm.Barrier()


def run_benchmark(cfg, storage_root="./", verify=True):
    comm.Barrier()
    if comm.rank == 0:
        shutil.rmtree(os.path.join(storage_root, DLIO_TEST_OUTPUT_DIR), ignore_errors=True)
    comm.Barrier()
    ConfigArguments.reset()
    workload_dict = OmegaConf.to_container(cfg["workload"], resolve=True)
    workload_dict.setdefault("output", {})["folder"] = DLIO_TEST_OUTPUT_DIR
    benchmark = DLIOBenchmark(workload_dict)
    benchmark.initialize()
    benchmark.run()
    benchmark.finalize()
    if comm.rank == 0 and verify:
        import glob
        output_pattern = os.path.join(benchmark.output_folder, "*_output.json")
        assert len(glob.glob(output_pattern)) == benchmark.comm_size
    return benchmark


def _sha256(path):
    h = hashlib.sha256()
    with open(path, "rb") as f:
        for chunk in iter(lambda: f.read(65536), b""):
            h.update(chunk)
    return h.hexdigest()


def _find_files(cfg, benchmark, ext):
    """Return (train_files, valid_files) sorted lists for a given extension."""
    data_folder = cfg.workload.dataset.data_folder
    train = sorted(pathlib.Path(f"{data_folder}/train").rglob(f"*.{ext}"))
    valid = sorted(pathlib.Path(f"{data_folder}/valid").rglob(f"*.{ext}"))
    return train, valid


